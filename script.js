function uno() {
    let notConnectedDiv = document.getElementById('beforeStart');
    let connectingDiv = document.getElementById('startedNotConnected');
    let connectedDiv = document.getElementById('connected');

    notConnectedDiv.style.display = 'block';
    connectingDiv.style.display = 'none';
    connectedDiv.style.display = 'none';
}
